    private void cargarTabla(int idUsuario){
        
        titulos[0] = "ID";
        titulos[1] = "Nombre";
        titulos[2] = "Estadio";
        titulos[3] = "Dirección";
        
        Leer miLeer = new Leer();
        Cargar miCargar = new Cargar();
        miCargar.cargarEquipos(tablaEquipoDos, miLeer.leerEquipos(idUsuario), titulos);
        
    }
    
