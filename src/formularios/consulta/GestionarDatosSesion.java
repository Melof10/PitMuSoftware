    private void tablaSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaSesionMouseClicked
        
        if (evt.getClickCount() == 2) {
            
            DatosDeSesionUsuario miDatosDeSesionUsuario = new DatosDeSesionUsuario();
            miDatosDeSesionUsuario.setFechaHoraInicioSesion(tablaSesion.getValueAt(tablaSesion.getSelectedRow(), 0).toString());
            miDatosDeSesionUsuario.setFechaHoraCierreSesion(tablaSesion.getValueAt(tablaSesion.getSelectedRow(), 1).toString());
            DatosDeSesionHistorial datosSesion = new DatosDeSesionHistorial(new javax.swing.JDialog(), true, miUsuario, miDatosDeSesionUsuario);
            datosSesion.setVisible(true);
            
        }      
        
    }//GEN-LAST:event_tablaSesionMouseClicked

