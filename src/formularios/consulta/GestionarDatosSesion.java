    private void txt_buscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_buscarKeyReleased
        
        Leer miLeer = new Leer();
        Cargar miCargar = new Cargar();
        DatosDeSesionUsuario datosSesion [] = miLeer.leerDatosDeSesionDinamicamente(miUsuario.getIdUsuario(), txt_buscar.getText().trim());    
        String titulos [] = new String [2];
        titulos[0] = "Fecha y Hora Inicio Sesión";
        titulos[1] = "Fecha y Hora Cierre Sesión";
        tablaSesion = miCargar.cargarDatosSesion(tablaSesion, datosSesion, titulos);
        
    }//GEN-LAST:event_txt_buscarKeyReleased

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
                        
        Leer miLeer = new Leer();
        Cargar miCargar = new Cargar();
        DatosDeSesionUsuario datosSesion [] = miLeer.leerDatosDeSesion(miUsuario.getIdUsuario());    
        String titulos [] = new String [2];
        titulos[0] = "Fecha y Hora Inicio Sesión";
        titulos[1] = "Fecha y Hora Cierre Sesión";
        tablaSesion = miCargar.cargarDatosSesion(tablaSesion, datosSesion, titulos);
        tablaSesion.getColumnModel().getColumn(0).setCellRenderer(columnasCentralizadas);        
        tablaSesion.getColumnModel().getColumn(1).setCellRenderer(columnasCentralizadas);
        
    }//GEN-LAST:event_formWindowOpened

