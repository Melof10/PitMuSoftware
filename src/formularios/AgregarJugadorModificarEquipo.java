    private void itemAgregarJugadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemAgregarJugadorActionPerformed

        Jugador miJugador = new Jugador();
        miJugador.setIdJugador(Integer.parseInt(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 0).toString()));
        miJugador.setNombreJugador(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 1).toString());
        miJugador.setApellidoJugador(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 2).toString());
        miJugador.setDniJugador(Long.parseLong(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 3).toString()));
        miJugador.setDomicilioJugador(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 4).toString());
        miJugador.setTelefonoJugador(Long.parseLong(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 5).toString()));
        miJugador.setEstaturaJugador(Double.parseDouble(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 6).toString()));
        miJugador.setPosicionJugador(tablaJugadorDos.getValueAt(tablaJugadorDos.getSelectedRow(), 7).toString());
        
        String miJugadorTabla[] = new String[8];
        miJugadorTabla[0] = String.valueOf(miJugador.getIdJugador());
        miJugadorTabla[1] = miJugador.getNombreJugador();
        miJugadorTabla[2] = miJugador.getApellidoJugador();
        miJugadorTabla[3] = String.valueOf(miJugador.getDniJugador());
        miJugadorTabla[4] = miJugador.getDomicilioJugador();
        miJugadorTabla[5] = String.valueOf(miJugador.getTelefonoJugador());
        miJugadorTabla[6] = String.valueOf(miJugador.getEstaturaJugador());
        miJugadorTabla[7] = miJugador.getPosicionJugador();
        
        Guardar miGuardar = new Guardar();        
        EquipoJoinJugador miEquipoJoinJugador = new EquipoJoinJugador();
        miEquipoJoinJugador.setIdEquipo(idEquipo);
        miEquipoJoinJugador.setIdJugador(miJugador.getIdJugador());
        miEquipoJoinJugador.setEstadoEquipoJoinJugador("alta");
        miGuardar.guardarEquipoJoinJugador(miEquipoJoinJugador);            

        new alertas.AlertSuccess(this, true, "El jugador se ha agregado correctamente").setVisible(true);                                           

    }//GEN-LAST:event_itemAgregarJugadorActionPerformed

