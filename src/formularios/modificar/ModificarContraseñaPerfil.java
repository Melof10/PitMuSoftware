    private void btn_mostrarPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrarPasswordActionPerformed
                
        if(mostrar){
            txt_mostrarPassword.setVisible(true);
            txt_password.setVisible(false);
            txt_mostrarPassword.setText(txt_password.getText());
            mostrar = false;
        }else{
            txt_password.setVisible(true);
            txt_mostrarPassword.setVisible(false);
            txt_password.setText(txt_mostrarPassword.getText());
            mostrar = true;
        }
        
    }//GEN-LAST:event_btn_mostrarPasswordActionPerformed

