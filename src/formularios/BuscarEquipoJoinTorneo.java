    private void itemAgregarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemAgregarEquipoActionPerformed
        
        Equipo miEquipo = new Equipo();
        miEquipo.setIdEquipo(Integer.parseInt(tablaEquipoDos.getValueAt(tablaEquipoDos.getSelectedRow(), 0).toString()));
        miEquipo.setNombreEquipo(tablaEquipoDos.getValueAt(tablaEquipoDos.getSelectedRow(), 1).toString());
        miEquipo.setNombreEstadioEquipo(tablaEquipoDos.getValueAt(tablaEquipoDos.getSelectedRow(), 2).toString());
        miEquipo.setDireccionEstadioEquipo(tablaEquipoDos.getValueAt(tablaEquipoDos.getSelectedRow(), 3).toString());
        
        String miEquipoTabla [] = new String [4];
        miEquipoTabla[0] = String.valueOf(miEquipo.getIdEquipo());
        miEquipoTabla[1] = miEquipo.getNombreEquipo();
        miEquipoTabla[2] = miEquipo.getNombreEstadioEquipo();
        miEquipoTabla[3] = miEquipo.getDireccionEstadioEquipo();
        
        NuevoTorneo.dft.addRow(miEquipoTabla);
        NuevoTorneo.tablaEquipo.setModel(dft);

        new alertas.AlertSuccess(this, true, "El equipo se ha agregado correctamente").setVisible(true);                                           
        
    }//GEN-LAST:event_itemAgregarEquipoActionPerformed

