    private void btn_IniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IniciarSesionActionPerformed
        
        if(!txt_password.getText().trim().isEmpty()){
                                   
            Modificar miModificar = new Modificar();                
                
            miUsuario.setPassword(txt_password.getText().trim());
            miModificar.modificarContraseñaUsuario(miUsuario);            
            dispose();                       
            
        }else{
            
            new alertas.AlertWarning(this, true, "Debe rellenar colocar una contraseña").setVisible(true);                                                       
            
        }        
        
    }//GEN-LAST:event_btn_IniciarSesionActionPerformed

