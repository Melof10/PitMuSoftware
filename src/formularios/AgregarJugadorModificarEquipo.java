    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed

        titulos[0] = "ID";
        titulos[1] = "Nombre";
        titulos[2] = "Apellido";
        titulos[3] = "DNI";
        titulos[4] = "Domicilio";
        titulos[5] = "Teléfono";
        titulos[6] = "Estatura";
        titulos[7] = "Posición";
        Leer miLeer = new Leer();
        Cargar miCargar = new Cargar();

        if (cmbDatos.getSelectedItem().equals("Nombre")) {
            tablaJugadorDos = miCargar.cargarJugador(tablaJugadorDos, miLeer.buscarJugadoresPorNombre(txt_buscar.getText().trim(), idUsuario), titulos);
        } else if (cmbDatos.getSelectedItem().equals("Apellido")) {
            tablaJugadorDos = miCargar.cargarJugador(tablaJugadorDos, miLeer.buscarJugadoresPorApellido(txt_buscar.getText().trim(), idUsuario), titulos);
        } else if (cmbDatos.getSelectedItem().equals("DNI")) {
            tablaJugadorDos = miCargar.cargarJugador(tablaJugadorDos, miLeer.buscarJugadoresPorDni(txt_buscar.getText().trim(), idUsuario), titulos);
        }

        propiedadesTabla();

    }//GEN-LAST:event_jToggleButton1ActionPerformed

